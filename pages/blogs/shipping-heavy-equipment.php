<h2><strong>Shipping Heavy Equipment After Preparation</strong></h2>
If you've chosen and are happy with a shipping company for your heavy equipment, take the following steps to make sure you're ready for pickup:
    <br><br>
<strong>Documents:</strong> You must ensure all permits and paperwork are in order before collection. You should confirm with the shipping company that the required visas are in place and have been obtained.
<br><br>
<strong>Check Condition and Take Pictures:</strong> Before you pick up your heavy equipment, you should check its condition and make sure it works.
<br><br>
It's a good idea to take high-quality photos with a time stamp, as this will give you great proof of the condition before any problems appear. Taking photos of any damage would also help.
<br><br>
<strong>Cover Critical Components:</strong> You should read the manual for your equipment and follow any instructions, such as turning off electrical components before transport.
<br><br>
Wrapping any of these 'vulnerable' components in bubble wrap or similar protective material is an excellent method to ensure the utmost security of your equipment.

